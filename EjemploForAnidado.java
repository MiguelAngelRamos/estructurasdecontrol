package estructurasjava;

public class EjemploForAnidado {
 
	public static void main(String[] args) {
	
		for(int j=0; j<=10; j++) {
			System.out.println("Imprimiendo la tabla del: " + j);
			for(int i=0; i<=10; i++) {
				System.out.println(j + "x" + i + " = " + (j*i));
			}
		}
		
		// linea codigo

	}

}
