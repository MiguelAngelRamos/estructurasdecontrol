package estructurasjava;

public class EjemploFor {

	public static void main(String[] args) {
		/* Imprimr Tabla de Multiplicar 2*/
		/* Un for es un ciclo */
		int tabla = 2; 
		// i = i + 1; abreviar i++
		for(int i=0; i<=10; i++) {
			// lo que se va repetir
			// expresa
			System.out.println(tabla  + "x" + i + "=" + (tabla * i));
		}
		
		// siguiente linea

	}

}
